    private boolean validaCapas(){
        for(int i = 0; i < this.pnlCapas.getComponentCount(); i++){
            JSpinner sp = (javax.swing.JSpinner)pnlCapas.getComponent(i);
            if (Integer.parseInt(sp.getValue().toString()) <= 0){
                JOptionPane.showMessageDialog(this, "El número de neuronas en la capa " + i + " debe ser mayor a cero." );
                return false;        }    
        }
        return true;
    }
    
