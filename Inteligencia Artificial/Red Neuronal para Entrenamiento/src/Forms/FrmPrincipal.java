    private void llenaMatriz(){
        matrizPatrones = new String[patrones.size()][patrones.get(0).length];
        for(int i = 0; i < patrones.size(); i++){
            for(int j = 0; j < patrones.get(i).length; j++){
                matrizPatrones[i][j] = patrones.get(i)[j];
            }
        }
    }
    
